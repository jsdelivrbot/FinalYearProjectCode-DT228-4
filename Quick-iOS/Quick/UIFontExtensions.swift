import UIKit

/// AvenirNext font family
extension UIFont {
  /// AvenirNext-Bold
  static func qFontBold(_ size: CGFloat) -> UIFont {
    return UIFont(name: "AvenirNext-Bold", size: size)!
  }
  /// AvenirNext-BoldItalic
  static func qFontBoldItalic(_ size: CGFloat) -> UIFont {
    return UIFont(name: "AvenirNext-BoldItalic", size: size)!
  }
  /// AvenirNext-DemiBold
  static func qFontDemiBold(_ size: CGFloat) -> UIFont {
    return UIFont(name: "AvenirNext-DemiBold", size: size)!
  }
  /// AvenirNext-DemiBoldItalic
  static func qFontDemiBoldItalic(_ size: CGFloat) -> UIFont {
    return UIFont(name: "AvenirNext-DemiBoldItalic", size: size)!
  }
  /// AvenirNext-Heavy
  static func qFontHeavy(_ size: CGFloat) -> UIFont {
    return UIFont(name: "AvenirNext-Heavy", size: size)!
  }
  /// AvenirNext-HeavyItalic
  static func qFontHeavyItalic(_ size: CGFloat) -> UIFont {
    return UIFont(name: "AvenirNext-HeavyItalic", size: size)!
  }
  /// AvenirNext-Italic
  static func qFontItalic(_ size: CGFloat) -> UIFont {
    return UIFont(name: "AvenirNext-Italic", size: size)!
  }
  /// AvenirNext-Medium
  static func qFontMedium(_ size: CGFloat) -> UIFont {
    return UIFont(name: "AvenirNext-Medium", size: size)!
  }
  /// AvenirNext-MediumItalic
  static func qFontMediumItalic(_ size: CGFloat) -> UIFont {
    return UIFont(name: "AvenirNext-MediumItalic", size: size)!
  }
  /// AvenirNext-Regular
  static func qFontRegular(_ size: CGFloat) -> UIFont {
    return UIFont(name: "AvenirNext-Regular", size: size)!
  }
  /// AvenirNext-UltraLight
  static func qFontUltraLight(_ size: CGFloat) -> UIFont {
    return UIFont(name: "AvenirNext-UltraLight", size: size)!
  }
  /// AvenirNext-UltraLightItalic
  static func qFontUltraLightItalic(_ size: CGFloat) -> UIFont {
    return UIFont(name: "AvenirNext-UltraLightItalic", size: size)!
  }
}
